d = float(input("Nhập điểm của bạn: "))
if d<3.5:
    print("Bạn xếp loại yếu")
elif 3.5<=d<5:
    print("Bạn xếp loại kém")
elif 5<=d<6.5 :
    print("Bạn xếp loại trung bình")
elif 6.5<=d<8 :
    print("Bạn xếp loại khá")
elif 8<=d<9 : 
    print("Bạn xếp loại giỏi")
else: 
    print("Bạn xếp loại xuất sắc")
    